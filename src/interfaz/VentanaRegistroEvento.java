    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
        // TODO add your handling code here:
        if ((nombreEventoTxt.getText().isEmpty())||(instructorEventoTxt.getText().isEmpty())||(montoEventoTxt.getText().isEmpty())){
            JOptionPane.showMessageDialog(null, "Campo(s) Vacio");
        }
        else {
        int d = fechaEvento.getCalendar().get(Calendar.DATE);
        int m = fechaEvento.getCalendar().get(Calendar.MONTH);
        int y = fechaEvento.getCalendar().get(Calendar.YEAR);
        control inicio = new control (d,m,y,nombreEventoTxt,instructorEventoTxt,montoEventoTxt);
        inicio.datosEvento(inicio.devolverEmpresa(), evento);
        VentanaMenuEventos ventana = new VentanaMenuEventos ();
        ventana.setLocationRelativeTo (null);
        ventana.setVisible(true);
        this.dispose();
        }
    }//GEN-LAST:event_agregarActionPerformed

