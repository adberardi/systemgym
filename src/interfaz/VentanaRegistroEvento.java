    private void nombreEventoTxtKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nombreEventoTxtKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (((c < 'A')||(c > 'Z'))&&((c < 'a')||(c > 'z'))&&(c != KeyEvent.VK_BACK_SPACE)&&(c != KeyEvent.VK_ENTER)&&(c != KeyEvent.VK_SPACE)){
            JOptionPane.showMessageDialog(null, "No se aceptan numeros");
            evt.consume();
        }
    }//GEN-LAST:event_nombreEventoTxtKeyTyped

    private void instructorEventoTxtKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_instructorEventoTxtKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (((c < 'A')||(c > 'Z'))&&((c < 'a')||(c > 'z'))&&(c != KeyEvent.VK_BACK_SPACE)&&(c != KeyEvent.VK_ENTER)&&(c != KeyEvent.VK_SPACE)){
            JOptionPane.showMessageDialog(null, "No se aceptan numeros");
            evt.consume();
        }
    }//GEN-LAST:event_instructorEventoTxtKeyTyped

    private void montoEventoTxtKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_montoEventoTxtKeyTyped
        // TODO add your handling code here:
         char c = evt.getKeyChar();
        if (((c < '0')||(c > '9'))&&(c != KeyEvent.VK_BACK_SPACE)&&(c != KeyEvent.VK_ENTER)){
            JOptionPane.showMessageDialog(null, "No se aceptan letras y/o espacios en blanco");
            evt.consume();
        }
    }//GEN-LAST:event_montoEventoTxtKeyTyped

